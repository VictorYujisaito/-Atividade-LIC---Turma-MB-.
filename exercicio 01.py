    A1=a1*2
    P1=p1*4
    A2=a2*3
    P2=p2*3
    media_final=(A1+P1+A2+P2)/12
    print('A1:',a1,'  ','P1:',p1)
    print('A2:',a2,'  ','P2:',p2)
